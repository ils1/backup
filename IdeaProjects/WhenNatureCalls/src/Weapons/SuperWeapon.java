package Weapons;

public class SuperWeapon {

    private String name;
    public void setName(String name){
        this.name = name;
    }
    public String getName(){
        return name;
    }

    private int damage;
    public void setDamage(int damage){
        this.damage = damage;
    }
    public int getDamage(){
        return damage;
    }
}
